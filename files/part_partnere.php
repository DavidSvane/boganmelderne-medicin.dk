	<p>
		<b>FADL's Forlag</b> er passionerede og professionelle bogelskere, som sætter samarbejdet med vores forfattere, de studerende og andre samarbejdspartnere i højsædet. Dette skaber rammerne for FADL’s Forlags bøger. Vi udgiver bøger til studerende inden for de sundhedsprofessionelle uddannelser og til den almindelige læser.
		<br /><br />Vores kerne er de medicinske fagbøger, og det har den været i over 60 år. Vi udgiver primært bøger til de medicinstuderende, men laver også bøger til den færdiguddannede læge, de sygeplejerskestuderende og andre sundhedsfaglige faggrupper.
		<br /><br />Vores fagbøger skrives af professionelle, dygtige og entusiastiske fagfolk. Vores fokus er altid på at opnå højeste kvalitet i både bogens indhold og form: Bøgerne er til at forstå og klæder læseren optimalt fagligt på. Vores bøger til den almindelige læser dækker et bredt spektrum af emner med afsæt i sundhed og livsstil. Vi laver kogebøger, populærvidenskabelige bøger, sundhedsbøger til familien,bøger om kost og motion og meget andet.
	</p>
	
