	<h2 id="eksamen">Eksamen</h2>
	<p>Der er 3 eksaminer på semestret.</p>
	<h4 id="ecelle">Kursus i medicinsk celle- og vævsbiologi og kursus i excitable celler</h4>
	<p>Dette er en 5 timer integreret skriftlig eksamen, som desuden indeholder billeder af histologiske præparater som skal angives. Det er kun tilladt at medbringe en lommeregner.</p>
	<br /><p>Eksamen ligger ca. 3 måneder inde i semestret.</p>
	<h4 id="egenetik">Kursus i medicinsk genetik</h4>
	<p>1 eksamen, som er skriftlig med en varighed på 2 timer. Bortset fra lommeregner, er eksamen uden hjælpemidler.</p>
	<h4 id="epsykologi">Kursus i sundhedspsykologi</h4>
	<p>1 eksamen, som er skriftlig med en varighed på 3 timer. Fagbogen og kompendiet må medbringes med indskrevne noter samt post it’s, men internetadgang er ikke tilladt.</p>
	<h3 id="goderad">Gode råd om eksamen</h3>
	<p>Det anbefales på det kraftigste, at du begynder at kigge på et par eksamensopgaver forholdsvis tidligt på semestret. Selvom du herved kan få en følelse af, at du ikke har særlig godt styr på stoffet, vil det dog give et indblik i eksamensspørgmålenes form. Desuden vil eksamensopgaverne give dig en forståelse af det niveau, som kræves til eksamen.</p>
	<br /><p>Til eksamenslæsningen gælder det især om at få dannet en strategi, således at du både får læst, samt lavet samtlige tidligere eksamensopgaver.</p>
