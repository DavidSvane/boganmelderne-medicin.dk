	<h2 id="generelt">Semesteret generelt</h2>
	<p>Velkommen på andet semester! Du er med stor sandsynlighed gode venner med Hole’s og den basale humanbiologi, og klar til at starte din forståelse af de cellulære processer, som ligger til grund for vores eksistens.</p>
	<br /><p>Til forskel fra første semester er læsebyrden større. Dette er både grundet at der simpelthen er flere sider, som skal læses, men også fordi indholdet er mere kompliceret. En anden nyhed er inklusionen af lysmikroskopi/virtuel mikroskopi. I histologiundervisningen (se nedenfor), vil noget af tiden benyttes på at lære, hvordan du identificerer forskellige vævstyper. Semestret indeholder desuden flere øvelser, end der var på første, og derfor skal der også skrives flere rapporter. Semestret er opbygget af to forløb.</p>
	<br /><p>I første forløb ligger ”kursus i medicinsk celle- og vævsbiologi”, hvilket indbefatter emnerne celle og væv, anatomi og biokemi, samt ”kursus i excitable celler”, som indbefatter faget biofysik.</p>
	<br /><p>I praksis deles fagene dog bedre ind i cellebiologi, histologi og biofysik, da det er sådan bøgernes inddeling passer bedst. Dette forløb varer samlet ca. 3 måneder.</p>
	<br /><p>Andet forløb indbefatter kurserne medicinsk genetik og sundhedspsykologi. Forløbet varer semestrets resterende halvanden måned.</p>
	
