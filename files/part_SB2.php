	<h3 id="forsteforlob">Første forløb - Kurses i medicinsk celle- og vævsbiologi og kursus i excitable celler</h3>
	<h4 id="cellebiologi">Cellebiologi</h4>
	<p>Faget dækker over forståelsen af cellens kemiske egenskaber, processer og funktioner. Dette er et af de største fag på semestret og når du begynder, kan det virke overvældende. Hvis du satser på at have læst til forelæsningerne, vil du muligvis blive overvældet. Mængden af nye enzymer og processer er betydelig. Det er derfor vigtigt at vide, at når du er over den initielle læsebyrde, bliver mængden af nyt stof i dette fag væsentlig mere afbalanceret.</p>
	<br /><p>Mht. de intracellulære signalveje, er det anbefalet, at du sætter dig og tegner dem et par gange hver dag i et par uger. Dette kommer dig til gode senere studiet, da det vil blive forventet, at disse signalveje er forstået.</p>
	<h4 id="histologi">Histologi</h4>
	<p>(histos = væv, logos = viden(skab)): Faget dækker over menneskekroppens forskellige væv, hvordan disse fungerer, samt deres mikroskopiske anatomi.</p>
	<br /><p>En stor del af faget handler om at lære, hvordan de forskellige typer væv i kroppen (f.eks. muskel-, binde-, nervevæv og epitheler) adskiller sig fra hinanden. Det betyder, at du til dels skal erhverve dig en forståelse af, hvordan de forskellige væv ser ud i et lysmikroskop/på virtuel mikroskopi (hvilket bliver anvendt til eksamen), men også at du skal forstå, hvorfor de ser sådan ud. Sidstnævnte gør, at der i dette fag også er mange proteiner og enzymer, som skal læres. Nogle lærebøger har meget høj detaljeringsgrad, mens andre kun medtager de mest essentielle.</p>
	<h4 id="excitableceller">Excitable celler</h4>
	<p>Faget beskæftiger sig med basal elektrofysik anvendt til at forstå hvordan især nerveog muskelceller kommunikerer og aktiveres. Desuden skal du lære en række intracellullære signalveje.</p>
	<br /><p>Selvom der ikke er meget læsning, kan faget være forståelsesmæssigt kompliceret. Du gør dig selv en stor tjeneste ved tidligt at få et godt kendskab til de elektrofysiske begreber såsom spændingsforskel, strømstyrke, modstand og kapacitet.</p>
	
	<h3 id="andetforlob">Andet forløb - Kursus i medicinsk genetik og kursus i sundhedspsykologi</h3>
	<h4 id="genetik">Medicinsk genetik</h4>
	<p>Faget beskæftiger sig med genkodens sammensætning – især med fokus på forskellen på det normale og det muterede gen.</p>
	<br /><p>Til forskel fra mange fag tidligt på bachelordelen, har genetik et meget klinisk fokus. Meget af den viden, som du erhverver dig på kurset ligger ikke blot til grund for hvad der skal læres senere – stoffet er i sig selv klinisk relevant.</p>
	<h4 id="sundhedspsykologi">Sundhedspsykologi</h4>
	<p>Faget fokuserer på at give en forståelse af sammenhængen kendt som den bio-psyko-sociale model, hvor kroppens mentale tilstand, fysiologiske niveau og sociale adfærd betragtes som en helhed. Derudover diskuteres emotionelle tilstande, som stress, samt hvad der ligger til grund for disse.</p>
	
