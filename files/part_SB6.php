	<h2 id="generelt">Semestret generelt</h2>
	<p>6. semester er det første semester på den kliniske del af studiet. Du vil tydeligt kunnemærke at du tager hul på en ny del af uddannelsen og en ny måde at få undervisning på. Undervisning foregår primært ude på hospitalerne, hvor du får undervisning af læger, der har en klinisk hverdag med patienter. Du kommer til at lære rigtigt lægearbejde som symptomatologi, diagnostik, udredning, differentialediagnoser, behandling og forebyggelse.</p>
	<br /><p>6. semester er første semester, hvor du ikke længere følger det hold, som du startede sammen med på 1.Semester BA. Undervisningen foregår stadig dels som forelæsninger og dels SAU, men nu med et nyt SAU-hold. Du kan komme på 3 forskellige hospitaler Herlev, Rigshospitalet og Bispebjerg/ Hvidovre.</p>
	<br /><p>Bispebjerg og Hvidovre skiftes til at have 6. semester hvert halvår. Alt i alt er 6. semester et rigtig spændende semester hvor du lærer meget om sygdomme.</p>
	
