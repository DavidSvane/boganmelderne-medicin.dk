	<h2 id="undervisning">Undervisning</h2>
	<p>Udover at have undervisning i sygdomslære, har du også undervisning i diagnostiske fag (radiologi, klinisk biokemi, klinisk fysiologi mm.), patologi og farmakologi. Derfor benyttes mange af 5. semesters fag igen på 6., hvor de yderligere uddybes. Undervisningen består af forelæsninger og af SAU-undervisning på hold af ca. 24 studerende. Alle underviserne på tema B, C og D er færdiguddannede læger, der ved siden af undervisningen har en klinisk hverdag med patienter. Dette gør undervisningen mere relevant og inspirerende, da de kan fortælle om deres patienter og gøre cases i SAU-undervisningen mere realistiske.</p>
	
