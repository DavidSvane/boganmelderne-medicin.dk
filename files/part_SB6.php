	<h2 id="fagene">Fagene</h2>
	<h4 id="temaa">Tema A</h4>
	<p>Udgøres af mikrobiologi. Undervisningen foregår på Panum og den består af forelæsninger og SAU-undervisning, samt øvelser i laboratorierne på Panum. Mikrobiologi kurset varer 3 uger (ca. 3 timer dagligt) og er obligatorisk. 2 uger af kurset omhandler bakteriologi og en uge omhandler virologi.</p>
	<br /><p>Øvelserne foregår fra kl. 13 til ca. kl. 16. Om formiddagen er der forelæsninger som ikke er obligatoriske.</p>
	<br /><p>På Absalon findes store oversigtsplancher over bakterier, vira, svampe og parasitter. Print dem og brug dem ved din læsning. Plancherne er ikke fyldestgørende, skriv noter til dem og lær dem udenad.</p>
	<br /><p>Et godt råd er at du bruger energi på at læse og få et godt overblik over bakteriologien, eftersom det er grundlaget for en stor del af sygdommene i tema B.</p>
	<h4 id="temab">Tema B</h4>
	<p>Omhandler infektionssygdomme og lungesygdomme. Infektionsmedicin hænger godt sammen med tema A, idet du lærer at bruge sin teoretiske viden om mikrobiologi på sygdomme hos mennesker.</p>
	<br /><p>De fleste infektionssygdomme, som du lærer om i dette tema er forårsaget af bakterier, hvilket giver et sammenspil mellem tema A og B.</p>
	<br /><p>Under lungemedicin beskæftiger du dig med lungernes sygdomme. Der er især fokus på Kronisk obstruktiv lungelidelse (KOL) og astma, der er de sygdomme du ser flest af på de lungemedicinske afdelinger.</p>
	<h4 id="temac">Tema C</h4>
	<p>Handler om de endokrinologiske sygdomme. Det en fordel at have styr på 5. semesters pensum om de endokrinologiske hormonakser. Den mest prominente sygdom i dette tema er diabetes mellitus (DM) type I og II.</p>
	<h4 id="temad">Tema D</h4>
	<p>Beskæftiger sig med bevægeapparatets sygdomme, det vil sige reumatologi som den medicinske del og ortopædkirurgi som den kirurgiske del.</p>
	<br /><p>Reumatologi omhandler bindevævssygdomme og kan deles i to grene, hvor den ene handler om gigtsygdomme, mens den anden handler om systemiske autoimmune sygdomme.</p>
	<br /><p>Ortopædkirurgi omhandler frakturer og skadestuekirurgi for både børn og voksne. Her skal du vide meget om kirurgiske behandlingsmuligheder og frakturklassifikationer.</p>
	
