	<h2 id="eksamen">Eksamen</h2>
	<p>Eksamen på sjette semester er blevet todelt. Den første og største er en 6-timers skriftlig integreret eksamen, uden hjælpemidler, fraset lommeregner uden lagrede data. Der vikomme spørgsmål indenfor infektionsmedicin, lungemedicin, endokrinologi, reumatologiog ortopædkirurgi. Spørgsmålene stilles primært som case-form. Til eksamen har de fleste tid til at læse deres eksamensbesvarelse igennem, hvis tiden struktureres ordenligt.</p>
	<br /><p>Den anden eksamen falder ca. en måned efter den første. Dette er en 3-timers skriftlig integreret eksamen, uden hjælpemidler, fraset lommeregner uden lagrede data. Det er multiple choice. Her vil der komme spørgsmål indenfor diagnostisk radiologi, patologisk anatomi og cytologi, klinisk genetik, klinisk mikrobiologi, klinisk immunologi, klinisk fysiologi og nuklearmedicin samt klinisk biokemi.</p>
	<br /><p>Du eksamineres desuden i din bacheloropgave på 6. semester. Denne eksamen koordinerer du dato for med din vejleder. Vær opmærksom på, at der er flere formalia din opgave skal opfylde, samt forskellige deadlines, du skal overholde. Se Kunet. For deadlines.</p>
