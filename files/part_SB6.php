	<h2 id="opbygning">Semestrets opbygning</h2>
	<p>Semesteret er delt i 4 temaer:</p>
	<ul>
		<li>Tema A: Mikrobiologi og farmakologi</li>
		<li>Tema B: Infektionsmedicin og lungesygdomme</li>
		<li>Tema C: Endokrinologi</li>
		<li>Tema D: Bevægeapparatets sygdomme</li>
	</ul>
	<p>Hvilket tema du starter med afhænger af hvilket hospital du ender på. Hvert tema tager 4 uger. Fakultetet har formelt inddelt alle temaerne i en diagnostisk og klinisk del. Der vil i praksis være undervisning i begge dele sideløbende, men du vil blive eksamineret i de to aspekter hver for sig. Du kan læse mere om eksamen længere nede i beskrivelsen.</p>
	<br /><p>Følgende kurser er fordelt ud på de 4 temaer:</p>
	<ul>
		<li><b>Kursus i diagnostiske fag:</b> Diagnostisk radiologi, patologisk anatomi og cytologi, klinisk genetik, kliniskmikrobiologi, klinisk immunologi, klinisk fysiologi og nuklearmedicin samt klinisk biokemi</li>
		<li><b>Kursus i kliniske sygdomsenheder:</b> Infektionsmedicin, lungemedicin, endokrinologi, reumatologi, ortopædkirurgi</li>
		<li><b>Mikrobiologisk øvelseskursus</b></li>
	</ul>
	
