	<h2 id="eksamen">Eksamen</h2>
	<h4 id="eojne">3 mundtlige eksaminer i hhv. hudsygdomme, øre-næse-hals-sygdomme og øjensygdomme</h4>
	<p>Disse tre eksaminer finder alle sted i løbet af en to-ugers periode i slutningen af semestret og er alle med karakterer. De to uger med eksamen betyder ikke at der er god tid imellem hver eksamen, da mange hold har to af dem med én eller ingen dage imellem. Heldigvis får man eksamensdatoerne i rigtig god tid, så man kan nå at overveje en læsestrategi.</p>
	<br /><p>Øre-næse-hals-eksamen foregår ved at man har 20 minutter til en (meget) fokuseret anamnese, og i princippet fuld ØNH-undersøgelse. Meningen er at man på denne tid også skal nå at skrive journalen, men dette bliver oftest bare til stikord. Det er heller ikke alle der når at lave den fulde undersøgelse, så det gælder selvfølgelig om at starte med det mest relevante. Til selve eksaminationen efterfølgende læses journalen op, og man bliver bedt om at demonstrere den del af ØNH-undersøgelsen der er særligt relevant for den pågældende patient. Derefter diskuterer man fund, diagnoser og behandling.</p>
	<br /><p>Eksamen i øjne er meget lig med ØNH, dog skal man optage anamnese og undersøge patienten med det samme foran eksaminator og censor. Herefter forlader patienten lokalet og man diskuterer fund, diagnoser og behandling. Måske studiets hurtigst overståede eksamen, da det hele er overstået på 20 minutter.</p>
	<br /><p>Dermatologi er anderledes, da der ikke er nogen patient til stede. Du trækker et nummer og får udleveret et antal billeder, der viser en en hudsygdom. Disse skal beskrives med den specielle dermatologiske terminologi, og derefter stiller eksaminator spørgsmål. Man dumper ikke hvis man ikke kan komme med den eksakte diagnose, hvis blot man kan beskrive billederne godt og komme med relevante differentialdiagnoser. Den videre del går mest af alt ud på at kunne fyre teori af direkte fra bogen, og man bliver gerne spurgt ind til mere end lige den sygdom, som billederne viser. Består man den første del af eksamen, trækker man endnu et kort hvor der står et emne inden for kønssygdomme (fx. Klamydia hos mænd), som man så går i gang med at øse ud af sin viden om. Igen kan man godt komme lidt bredere rundt, da der ofte er fokus på differentialdiagnoser. Både dermatologien og venerologien skal bestås, og den samlede karakter er vægtet 2:1.</p>
	<br /><p>Gældende for alle tre eksamener er, at din eksaminator vil være den samme som har undervist i de kliniske lektioner. Derfor vil han eller hun have haft mulighed for at fortælle, hvad han eller hun ønsker til sine eksaminer.</p>
