	<h2 id="eksamen2">Eksamen</h2>
	<h4>Medicinsk sociologi</h4>
	<p>Dette er en 3 timers skriftlig eksamen, hvor hjælpemidler er tilladt. Spørgsmålene er stillet i lighed med de cases der bliver brugt i SAU-timerne.</p>
	<h4>Eksamen i medicin, kirurgi, patologi, farmakologi og anæstesiologi</h4>
	<p>Denne eksamen er en 6-timers skriftlig, integreret eksamen uden hjælpemidler, fraset lommeregner. Der kan blive spurgt ind til alt det pensum som er gennemgået på 6., 7., 8. og 9. semester, dvs. al intern medicin og kirurgi samt anæstesiologi.</p>
	<br /><p>Typisk vil der være 4 hovedopgaver, hvilket vil sige at man langt fra bliver spurgt til alle emner.</p>
	<h4>OSCE</h4>
	<p>Denne eksamen er på alle måder anderledes end dem der har været på de tidligere semestre. Det er en blandet praktisk, skriftlig, mundtlig eksamen, som består i 12 poster med forskellige opgaver. De skriftlige poster kan fx være spørgsmål til billeder (patologisk, billeddiagnostisk), de praktiske kan være procedure-orienterede, fx anlægge PVK, tage a-punktur, anlæggelse af larynx-maske, eller undersøgelses-orienterede hvor man skal undersøge fx ankel, knæ eller andet på en ’patient’. De mundtlige poster kan enten være rent teoretiske, hvor eksaminator stiller spørgsmål, eller der kan være en ’patient’, som man skal informere eller stille spørgsmål til.</p>
	<br /><p>Man skal ikke selv medbringe hjælpemidler.</p>
	
