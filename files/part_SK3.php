	<h2 id="fagene">Fagene</h2>
	<h3 id="ojne">Øjne, hud, kønssygdomme, ører, næse og hals</h3>
	<p>Efter at have været på adskillige kliniske semestre, har man efterhånden vænnet sig til, hvordan disse indeholder ophold i klinikken i alle ugens dage. Men sådan skal det ikke være på kandidatens femte semester!</p>
	<br /><p>Her er klinikken indrettet således, at man én dag om ugen har 3 timers dermatologi, en anden dag har 3 timers oto-rhino-laryngologi, en tredje dag har 3 timers oftalmologi og fredage er der 3-4 forelæsninger. Heldigvis er det indrettet sådan at de forskellige ugedage altid indeholder det samme fags undervisning, og underviseren er den samme hele semestret (og eksamen) igennem.</p>
	<br /><p>Overordnet er det sådan, at både øre-næse-hals og oftalmologi er meget fokuseret på, at man kan lave de respektive objektive undersøgelser korrekt. Teoretisk viden er vigtigt, men ikke i samme grad, som i andre kliniske fag. Dermatologi har ikke nogen egentlig objektiv undersøgelse. Dermaskopi er en specialistopgave, som studerende ikke forventes at kunne. Derfor er derma noget mere fokuseret på teori, hvor især billeder af diverse hudsygdomme er vigtige. Kønssygdomme hører også til i derma, men det betyder ikke at man skal finde alle sine gamle noter om HIV og Hepatitis A-E frem fra skuffen, da der næsten udelukkende er fokus på deres hudsymptomer.</p>
	<h3 id="statistik">Statistik, epidemiologi og medicinsk sociologi</h3>
	<p>Medicinsk sociologi består af oversigtsforelæsninger og SAU-timer, hvor man typisk vil gennemgå en artikel der belyser sundhedsmæssige forskelle mellem forskellige befolkningsgrupper. Dertil kommer forelæsningerne om det danske sundhedsvæsens opbygning, kvalitetssikring og økonomi.</p>
	<br /><p>Statistik og epidemiologi er metodefag, hvor man lærer at bruge og forholde sig kritisk til kvantitative data, for at kunne sige noget om årsagssammenhæng. Fx: Hvis der er overdødelighed blandt folk der har lightere i hjemmet, betyder det så at lightere er sundhedsskadelige? Eller er det bare fordi folk der ryger, oftere har lightere i hjemmet end folk der ikke ryger?
	<h3 id="miljo">Miljø- og arbejdsmedicin</h3>
	<p>Der bliver afholdt store forelæsninger og derudover bliver der også afholdt SAU timer. SAU timerne foregår som case timer, hvor der er en patient med til timen, som har været igennem et forløb, omhandlende det emne som man er i gang med.Formålet med kurset er at kende til forskellige former for arbejdsskader og arbejdsrelaterede sygdomme, og kunne angive en vurdering af hvornår noget kan anmeldes som arbejdsskade. Her kan man vælge at læse det i pensum bogen, eller man kan læse noter (Asma Bashir) med tilskud fra forelæsningerne.</p>
	
