	<h2 id="fagene2">Fagene</h2>
	<h3 id="sociologi2">Medicinsk sociologi og sundhedsvæsenets struktur</h3>
	<p>Kurset består af oversigtsforelæsninger og SAU-timer, hvor man typisk vil gennemgå en artikel der belyser sundhedsmæssige forskelle mellem forskellige befolkningsgrupper. I den sidste uge omhandler forelæsningerne det danske sundhedsvæsens opbygning, kvalitetssikring og økonomi.</p>
	<h3 id="intern2">Kliniske kurser i intern medicin II og kirurgi II</h3>
	<p>Ligesom på 7. semester er man i klinik fuld tid. På 9. semester har man været igennem al den interne medicin og kirurgi og det er nu meningen at man skal opfriske og afprøve sin viden i praksis. Foruden de praktiske færdigheder som står i logbogen, skal man ud fra anamnese og objektiv undersøgelse lære at tænke i differentialdiagnostiske muligheder og videre undersøgelser. På mange måder virker dette kursus som en gentagelse af 6. og 8. semester, men forskellen er, at hvor man på Peter Bangsvej kan ordinere en MR-scanning eller en EKKO for at være på den sikre side, så bliver man i klinikken nødt til at overveje om det er realistisk at få igennem og hvad man stiller op hvis der er kø til scanneren (og det er der altid!).</p>
	<h3 id="an2">Klinisk kursus i anæstesiologi</h3>
	<p>Opholdet på anæstesiologisk afdeling er kort, og ofte går der et par dage med teoretisk undervisning, rundvisning osv, men man kan alligevel nå at se en masse. Man skal lære nogle generelle principper om anæstesi og smertebehandling, og så nogle praktiske ting som fx at ventilere på maske, intubere, tage Apunktur.</p>
	
