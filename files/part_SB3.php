	<h2 id="tko">Tidlig klinisk ophold (TKO)</h2>
	<p>TKO består af et 7 dages ophold på enten en tildelt eller selvfundet afdeling (KU-arrangeret eller selvarrangeret tidligt klinisk ophold). Meningen med dette ophold er at give den studerende et kendskab til en læges daglige arbejde. Ydermere er det meningen at du skal stifte bekendtskab med dagligdagen på et sygehus og dets forskellige faggrupper og selvfølgelig patienter.</p>
	<br /><p>På opholdet følger du lægerne i deres arbejde og hverdag. Det er meget forskelligt, hvor meget du får lov til at lave. Men nogle steder kan du få lov til at tage del i arbejdet. Det kan f.eks. være ved at lægge forbinding, lægge perifert venekateter (PVK), suturere, teste reflekser osv. Du kan også nogle steder få lov til at assistere til operationer.</p>
	<br /><p>Kurset ligger på den sidste del af 3. Semeser og der er ingen eksamen i kurset. Dog skal du have udfyldt en blanket med lægens, eller afdelingens stempel efter endt forløb. Denne skal herefter afleveres på studieservice centeret (SSC). Kurset kan første godkendes efter du har bestået både 1. og 2. Semester. Desuden skal du bestå dette kursus inden du bliver bachelor i medicin.</p>
