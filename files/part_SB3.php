	<h3 id="forsteforlob">Første forløb - Kursus i hoved, hals, bevægeapparatet og det perifere nervesystem</h3>
	<br /><p><b>Bevægeapparatets anatomi:</b> Anatomien bag armen, benet, abdomen, hoved og hals.</p>
	<br /><p><b>Embryologi:</b> Anatomien og udviklingen af embryonet.</p>
	<br /><p><b>Dissektion:</b> Dissektion af forskellige regioner på en doneret menneskekrop.</p>
	<br /><p>Kurset giver den studerende en anatomisk viden, terminologi og en beskriveteknik, som er uundværlig ved patientundersøgelser, samt danner basis for kommunikation med både kommende kollegaer og andet sundhedsfagligt personale. På dissektionskurset kommer du til at dissekere rigtige lig, hvilket er en unik mulighed for at få praktisk erfaring med anatomi.</p>
	<br /><p>Anatomien, som gennemgås, omhandler ekstremiteterne (arme og ben), hovedet og halsen, samt maveog thoraxmuskulaturen. Du lærer anatomien for knogler, muskler, nerver og kar inden for de angivne områder. Denne del af det første forløb varer 6 uger. På dette kursus forekommer SAU-timerne før forelæsningen. Det er derfor en rigtig god ide at læse til SAU. Derefter vil pensum som blev gennemgået i SAU, blive hurtigt gennemgået til forelæsningerne.</p>
	<br /><p>Efter anatomien er blevet gennemgået starter dissektionskurset som varer i 4 uger. Sideløbende med dissektionskurset gennemgås embryologien. Dvs. at du stadig har forelæsninger og SAU timer.</p>
	<br /><p>Anatomi er meget anderledes at læse, end det du er vant til fra 1. og 2. semester. Du skal derfor ikke blive nervøs, hvis det tager flere timer at komme igennem 2-4 sider. Desuden er det meget forskelligt, hvordan studerende lærer. Nogle skal tegne det hele, andre lærer bedst ved at skrive udførlige noter, mens andre igen bruger anatomiske flashcards eller noget helt fjerde. Vi anbefaler derfor, at du i starten prøver forskellige studieteknikker for at finde ud af, hvad der fungerer bedst for dig.</p>
	
	<h3 id="andetforlob">Andet forløb - Centralnervesystemets struktur og funktion</h3>
	<br /><p><b>Neuroanatomi:</b> Anatomien af strukturer der befinder sig indenfor de tre hjernehinder.</p>
	<br /><p><b>Neurofysiologi:</b> Sanser, motorik og højere kortikale funktioner.</p>
	<br /><p>I dette andet forløb af semestret vil du beskæftige dig med anatomien indenfor centralnervesystemet (CNS). Du går her i dybden med hjernen, rygmarven, kar osv. som befinder sig inden for hjernehinderne. Desuden vil du lære lidt klinik omkring ledningsbaner og reflekser. Ydermere vil du lære hvordan du udfører en neurologisk undersøgelse af en patient.</p>
	<br /><p>Udover anatomien kommer du desuden til at stifte bekendtskab med neurofysiologi, hvor der i kurset lægges vægt på mekanismerne bag bl.a. smerte, hørelse, synssans, hukommelse, førelser osv.</p>
	
