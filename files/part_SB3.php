	<h2 id="generelt">Semestret generelt</h2>
	<p>Velkommen til tredje semester. Mange studerende vælger at gøre en meget stor indsats på dette semester. Det er vigtigt, at du som studerende, forinden semesterstart gør dig klart, hvor meget tid du vil bruge på at studere. Du skal derfor passe på ikke at lade dig påvirke af andres umiddelbare kunnen, prioritering og tidsforbrug på studiet.</p>
	<br /><p>På tredje semester forekommer 2 separate kurser som ligger efter hinanden. Det første kursus hedder Hoved, hals, bevægeapparatet og det perifere nervesystem og omhandler anatomien omkring knogler, muskler, led, nerver og kar. Det andet kursus hedder Centralnervesystemets struktur og funktion og omhandler anatomien og fysiologien omkring det centrale nervesystem (CNS).</p>
	
