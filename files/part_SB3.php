	<h2 id="eksamen">Eksamen</h2>
	<h4 id="ehovedhals">Kursus i hoved, hals, bevægeapparatet og det perifere nervesystem</h4>
	<p>Eksamen for dette kursus starter en uge efter afsluttet dissektionskursus. Denne eksamen består af to dele - en spoteksamen med 16 præparater, hvor der er 2 minutter og 10 sekunder til hver. Til hver af de 16 præparater er der 2 spørgsmål, hvor man skal angive navne på de 2 viste strukturer.</p>
	<br /><p>Den anden del af eksamen er en mundtlig præsentation af to angivne spørgsmål med 30 minutters forberedelse og 30 minutters eksamination. Spotprøven foregår på én dag for alle studerende på semestret, mens den mundtlige eksamen strækker sig over en uge eller mere. Spotprøven forløber før den mundtlige eksamen.</p>
	<br /><p>Til den mundtlige eksamen ligger der følgende 3 atlasser, som også kan findes i boglisten.</p>
	<ul>
		<li>Atlas Der Anatomie (Netter)</li>
		<li>Sobotta, Atlas of anatomy (Hvide eksemplar med muskelkompendiumet, 15. Udgave i 3 bind)</li>
		<li>Thieme, Atlas of Anatomy i 3 bind</li>
	</ul>
