	<h2 id="undervisningsform">Undervisningsform</h2>
	<p>Det er ikke sikkert, at du kan nå at læse til hver time. Det er derfor vigtigt, at du lærer at prioritere din tid. Et godt råd er at læse til SAU timerne først, således at du kan følge nogenlunde med her. Altså kan det anbefales at læse til SAU først - og derefter læse så meget, som du kan nå til forelæsningerne. Over SAU vil vi dog anbefale, at du prioriterer dissektion højst, når det kursus begynder. SAU timerne på dette semester benævnes som følgende:</p>
	<ul>
		<li>AX: Anatomisk demonstration - ved SAU-lærer</li>
		<li>AK: Klinisk anatomi - ved lektor/ læge</li>
	</ul>
	<br /><p>Makroskopisk studiesal (aka. Tør, tør studiesal) giver dig mulighed for at få en bedre rumlig forståelse, samt syn på hvor de forskellige kar, nerver og muskler ligger ift. hinanden. På tør findes også 5 Imacs computer. Her kan du øve dig i at se på MR, CT og røntgenbilleder, og det er vigtigt ift. spot eksamen.</p>
	<br /><p>Som medicinstuderende i København, har du den unikke mulighed, at kunne gå på våd studiesal. Våd studiesal ligger i kælderen, lokale 18.01.42. Her kan du stå med præparaterne i hænderne og dermed få en rummelig forståelse. Der er altid en SAU-underviser eller dissektionsvejleder på våd studiesal, som med glæde vil hjælpe dig med alle de spørgsmål, som du end måtte have. Vi anbefaler, at du fra starten af semestret både benytter våd og tør studiesal. Det er et godt afbræk fra bøgerne, og det er en god ide, at supplere sin læsning om fx lårets muskler, ved at gå på tør eller våd og kigge på det.</p>
	<br /><p>Tør har altid åben. Våd derimod har ikke altid åben, men åbningstiderne står på døren indtil lokalet (lok. 18.01.42), eller i MOK.</p>
	
