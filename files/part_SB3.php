	<h2 id="forlobet">Forløbet</h2>
	<p>I begyndelsen af semesteret bruger du de første to uger på overekstremiteten (OE). Derefter skal du have om underekstremiteten (UE) på en uge. Dernæst begynder det at gå stærkt, fordi de resterende emner såsom truncus, hoved og hals, embryologien osv. kommer til at overlappe hinanden.</p>
	<br /><p>Efter 6 uger starter dissektionskurset som varer 4 uger med dissektion 2-3 gange om ugen (i alt 8 gange à 3 timer). Denne del af kurset tager en del af ens tid, fordi du skal forberede dig og være klar på de spørgsmål, som dissektionsvejlederne stiller. Dissektionstimerne er meget udbytterige, hvis du møder velforberedt op til dem. De fleste er trætte efter dissektion, fordi du bruger mange kræfter på det. Det er derfor en god ide at regne med, at du har brug for en pause efter dissektion. Sideløbende med dissektionen forløber embryologiundervisningen.</p>
	<br /><p>Ca. 1-3 dage efter kurset i Hoved, hals, bevægeapparatet og det perifere nervesystem starter det andet kursus. Kurset Centralnervesystemets struktur og funktion varer 6 uger og bygger videre på den viden, som du fik omkring det perifere nervesystem.</p>
	
