	<p class="description">Basal og klinisk farmakologi, også kendt som “den flotte farmabog”, anvendes som pensumbog i faget farmakologi på femte semester og bliver mere supplerende undervejs på resten af studiet. Bogen er inddelt i tre dele med en basal del til generelle principper, en farmaka del, som kigger nærmere på farmakoterapi og organrelateret farmakologi. Den tredje og sidste del omhandler den kliniske farmakologi, hvor der fx gennemgås bivirkninger, interaktioner, akutte forgiftninger, doping og meget andet. Den nye udgave er nu i en smuk, smuk grønblå farve (heraf navnet jf. intro - medicinerjargon er nice!), nøjagtig magen til den skjorte du fik af din mormor sidste jul og aldrig har brugt. Den er desuden i “hardback”, hvilket jo altid er dejligt. Femte udgave er opdateret med nye lægemidler og viden om farmakologiens verden, men ikke i en sådan grad, at du dumper eksaminer, fordi du læser ældre versioner.<br /><br />

Bogen har nogle gode indledende kapitler om den basale farmakologi. Disse kapitler er skrevet fornuftigt og deres opbygning er velstruktureret på en sådan måde, at den studerende opnår en god forståelse for de basale emner. Kapitlerne i farmakadelen og den kliniske del er mange og korte. De tager i farmakadelen hver især udgangspunkt i et organsystem eller en sygdomsgruppes behandling. Ulemperne herved er at bogen kan virke en smule usammenhængende, da hvert emne behandles uafhængigt af hinanden. Dette er dog kun et problem ved førstegangslæsningen, da man senere bliver glad for at kunne slå et specifikt emne op, uden at skulle lede efter det i et mere omfattende kapitel. Bogen går mindre op i at beskrive de enkelte farmaka ift. virkningsmekanisme, kinetik, indikation, kontraindikationer, bivirkninger og interaktioner. Disse informationer kan i stedet hentes på pro.medicin.dk. Denne overordnede tilgang til farmakologien gør bogen god til førstegangslæsning.<br />
Værket er også godt til at koble farmakologien og klinikken sammen. Den beskriver ofte hvordan man vælger den rette behandling, og indikationerne herfor. Sidstnævnte er dog mest relevant fra sjette semester og senere på studiet, hvor man skal kende til behandlingen af diverse sygdomme. Det skal derfor nævnes at det først er på disse semestre og ikke på femte, at man skal kunne redegøre for komplicerede behandlingsmodaliteter.<br /><br />

Sammenligning med Golan’s Basic Principles of Pharmacology:<br />
Detaljeringsgraden i Golan er et stykke over “den flotte farmabog” uden at det nødvendigvis er en fordel. Golan er således detaljetung i teksten, men opsummerer vigtige pointer ved tabeller og billeder og krydrer det med kliniske cases, som du kender det fra andre amerikanske lærebøger, som “blåhvalen aka Boron”. Om du foretrækker den “amerikanske” lærebogsstil fremfor den danske ved kun du. Fordelen ved den danske er dog, at nogle amerikanske valg af lægemiddel til en given sygdom kan deviere en del fra dansk klinisk praksis. Dette kan dog løses ved at man kigger på promedicin i ny og næ. <br /><br />

Basal og klinisk farmakologi er en fyldestgørende dansk lærebog til farmakologien på hele medicinstudiet og til dit fremtidige virke som læge. Den giver en god beskrivelse af både den basale farmakologi, samt en sygdomsorienteret tilgang til anvendelsen af farmakologiske produkter i den kliniske situation. Den anbefales til læseren, som har det fint med at slå specifikke farmaka op på sider såsom pro.medicin.dk. Uanset hvilken bog du vælger i farmakologi anbefales det at supplere med farmakort, som det beskrives i semesterbeskrivelsen for femte semester.
