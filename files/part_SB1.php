	<h2 id="eksamen">Eksamen</h2>
	<p>Se udførlig eksamensoversigt på: <a href="http://sund.ku.dk/uddannelse/vejledning-information/eksamensplaner/medicin/" target="_blank"><b>Eksamensplaner</b></a></p>
	<h3 id="ehbk">Eksamen i humanbiologi og kemi</h3>
	<p>Integreret skriftlig eksamen af 4 timers varighed. Du må medtage dit tabelværk over kemiske data og en lommeregner. Det er ikke tilladt at have gemt data på lommeregneren – de tager stikprøver til eksamen, så overhold reglerne for din egen skyld. Der gives ikke karakterer for eksamen, men bestået/ikke bestået som resultat. Eksamen skal være bestået inden du kan påbegynde 3., 4. og 5. semester – men det er altså muligt at fortsætte på 2. Semester uden at have bestået 1. semester. Der afholdes midtvejs i semesteret en prøve-eksamen/midtvejstest i eksamenslokalerne på Peter Bangs vej, så man har mulighed for at lære faciliteterne at kende. Denne opgave rettes ikke, men den studerende får selv besvarelsen tilsendt, så man har mulighed for selv at rette den ud fra rettevejledningen</p>
	<h3 id="etpk">Eksamen i TPK – Tidlig Patient Kontakt</h3>
	<p>Eksamen er mundtlig og varer cirka 20 minutter. Der er ingen forberedelsestid til eksamen, da du eksamineres ud fra din tidligere afleverede rapport. Til eksamen stilles der både konkrete spørgsmål til rapporten, samt perspektiverende spørgsmål til pensum. Du må selvfølgelig gerne have din rapport med ind til eksamen – Men du må IKKE have lavet noter, overstregninger eller lignende i rapporten. Eksamen skal være bestået inden du kan påbegynde 3., 4. og 5. Semester.</p>
