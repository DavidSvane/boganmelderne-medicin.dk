	<h3 id="intro">Introduktionskursus</h3>
	<p>Medicinstudiet skyder starten af med et kort introduktionskursus, som blandt andet indbefatter et én dags hospitalsbesøg. Dagen fungerer som en erhvervsdag på et hospital. Lægerne, som vejleder, tror man kan en masse nu når er begyndt på studiet, så der er mulighed for gode oplevelser med lidt at lave. Ældre medicinstuderende tilknyttes hvert hold som tutorer og introducerer den daglige gang. Blandt andet introduceres Studievejledningen. Kurset fungerer som en generel introduktion til dagens gang på Panum.</p>
	
