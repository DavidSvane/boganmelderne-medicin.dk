	<h2 id="former">Undervisningsformer</h2>
	<h3 id="lecture">Forelæsninger</h3>
	<p>Forelæsningerne varer 45 min, starter et kvarter over et helt timetal (det akademiske kvarter) og omhandler et givet emne fra pensum. Det er meget forskelligt blandt de studerende, i hvilken grad de kommer til forelæsningerne. Et godt råd er, at du lægger ud med at komme til alle forelæsningerne og selv finder ud af, hvad det giver dig – og om du skal skrive noter på computer eller papir, eller måske foretrækker bare at sidde og lytte.</p>
	<h3 id="sau">SAU undervisning</h3>
	<p>SAU undervisning eller ”Studenter-Aktiverende Undervisning” foregår som almindelig klasseundervisning. Her har de studerende mulighed for at stille spørgsmål til pensum og være en del af undervisningen. SAU undervisning prioriteres højt af de fleste studerende, da mange finder det meget udbytterigt. På 1. semester har du SAU undervisning i humanbiologi. Her er underviseren enten en studerende fra et højere semester eller en færdiguddannet læge. I faget kemi er SAU underviseren ofte en specifikt faguddannet fastansat ved fakultetet.</p>
	<h3 id="cafe">Café-timer</h3>
	<p>I kemi er der arrangeret café timer, som foregår i et af auditorierne. Café timerne foregår sådan, at de studerende har mulighed for at komme og regne opgaver. Der vil være to lærere, der står til rådighed for at besvare jeres spørgsmål og gennemgå emner som fx pH eller Fischer til Hayworth projektion på tavlen.</p>
	
