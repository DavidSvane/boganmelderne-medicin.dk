	<h3 id="bhckk">Kursus i Basal HumanBiologi &amp; Cellens Kemiske Komponenter</h3>
	<p>I daglig tale er BHB kendt som “Humanbiologi” og Cellens Kemiske Komponenter som før hed “Medicinsk Kemi”, nu bedre kendt som “Kemi”.</p>
	<br /><p>Formålet med kurset i humanbiologi og cellens kemiske komponenter er overordnet at give den studerende et overblik, samt grundlæggende forståelse for den menneskelige organismes kemiske og anatomiske opbygning.</p>
	<br /><p>Deltagelse i laboratorieøvelser i cellens kemiske komponenter og efterfølgende aflevering af rapporter er <i>obligatorisk</i> for at kunne bestå kurset og få lov til at gå til eksamen.</p>
	<h4 id="ovelser1">Øvelser på kurset i humanbiologi og kemi</h4>
