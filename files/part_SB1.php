	<h2 id="generelt">Semesteret generelt</h2>
	<p>I de første uger af første semester har du et introduktionskursus. Kurset tjener som en almen introduktion til selve medicinstudiet, undervisningsmetoderne, samt de studieadministrative redskaber på fakultetet. I slutningen af kurset er du ude på et éndags ophold på en given hospitalsafdeling. Her ser du hvordan læger arbejder, samt hvordan en afdeling på et hospital fungerer. Der er ingen obligatoriske elementer forbundet med kurset. Efter introduktionskurset starter selve semestret. Du har et integreret kursus i <a href=""><b>basal humanbiologi</b></a> og <a href=""><b>cellens kemiske komponenter</b></a> sideløbede med et kursus i tidlig patient kontakt.</p>
	<br /><p>Hvis du vil se ECTS (European Credit Transfer System) point for kurserne, målbeskriv- elser for fagene og lignende, tryk her: <a href="http://sund.ku.dk/uddannelse/vejledning-information/studieordninger/medicin/" target="_blank"><b>KUnet - Kursusbeskrivelser</b></a></p>
	
