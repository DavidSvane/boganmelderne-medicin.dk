	<h4 id="rad2">Gode råd til studiet og semestret generelt</h4>
	<p>Som mange studerende også vil fortælle jer, er den bedste forberedelse til eksamen ofte at regne gamle eksamenssæt. Derfor er café-timerne en rigtig god ide at komme til, da I netop her får regnet de ”typeopgaver” igennem, som vil blive stillet til eksamen.</p>
