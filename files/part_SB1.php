	<h3 id="tpk">Kursus i Tidlig Patient Kontakt (TPK) (heraf 3 dage i klinik)</h3>
	<p>TPK kurset sigter mod at give den studerende en dybere og bredere forståelse for lægens rolle og funktion som kommunikator, samt læge-patient forholdet. Du vil blive undervist ud fra forskellige modeller og teorier, og skal ved endt kursus have en forståelse af forskellige perspektiver på sygdom, samt betydningen af det enkelte menneskes livsforløb på tolkningen og forståelsen af egen sygdom.</p>
	<br /><p>På kurset lærer du basale kommunikationsteknikker, grundlæggende etiske overvejelser i lægeprofessionen mv. Du vil under forløbet blive tilknyttet en praktiserende læge, som vil tildele dig en patient. Du skal gennemføre 3 samtaler med henholdsvis lægen og patienten, hvor I snakker om patientens sygdom, livsforløb, forhold til sundhedssektor o.a. Samtalerne skal munde ud i en rapport, hvor du redegør for patientens egne overvejelser omkring sygdom og lignende (se kompendiet i TPK for formalia til opgaven). Det kan anbefales at gøre et godt stykke arbejde ud af rapporten, da <i>det er denne du eksamineres ud fra</i>, og det er en god måde at få anvendt og forstået pensum på.</p>
	<h4 id="obl1">Obligatoriske elementer</h4>
	<ul>
		<li>Førstehjælpskursus: Et to dages kursus i basal førstehjælp på Panum.</li>
		<li>Undervisning i tavhedspligt. SAU-undervisning (første time) med din lærer på Panum.</li>
		<li>Besøg hos praktiserende læge og patient, samt aflevering af rapport, der opfylder de formelle krav.</li>
	</ul>
	
