	<h4 id="rad1">Generelle råd til øvelser</h4>
	<p>Laboratorieøvelserne foregår nogenlunde, som du er vant til i gymnasiet. Du skal dog være opmærksom på, at du selv er ansvarlig for at møde velforberedt op til øvelsen, altså skal du som minimum have læst øvelsesvejledningen. Det kan anbefales at møde velforberedt til øvelserne, da der oftest ikke er sat tid af til teoretisk gennemgang før øvelsen begynder. HUSK at undersøge om du selv skal medbringe kittel eller handsker eller om en eller flere personer fra holdet skal melde sig som forsøgspersoner inden øvelsesdagen.</p>
	
